package com.sample.model;

public class HelloError {

	private String error;

	public HelloError() {
		super();
	}

	public HelloError(String error) {
		super();
		this.error = error;
	}

	public String getError() {
		return error;
	}

	public void setError(String error) {
		this.error = error;
	}

}
